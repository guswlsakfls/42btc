#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long long	nb;

	nb = n;
	if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		nb *= -1;
	}
	if (nb > 10)
		ft_putnbr_fd(nb / 10, fd);
	ft_putchar_fd("0123456789"[nb % 10], fd);
}
